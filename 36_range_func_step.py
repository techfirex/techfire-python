# range(start, stop, step)

for i in range(1,11,1):
    print(f"1 to 10 : {i}")

print("--------------")

for i in range(1,11,2):
    print(f"odd : {i}")

print("--------------")

for i in range(0,11,2):
    print(f"even : {i}")
   
print("--------------")
 
for i in range(10,0,-1):
    print(f"reverse : {i}")

print("--------------")

for i in range(-1,-11,-1):
    print(f"reverse : {i}")
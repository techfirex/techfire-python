# and & or

name = "tushar"
age = 21

# and
# both condition true = if block code run
# any one condition false = else block code run
if name=="tushar" and age==21: 
    print("condition true")
else:
    print("condition false")
    
# or
# both condition true = if block code run
# any one condition true = also if block code run
# all condition false = then else block code run
if name=="tushar" or age==25:
    print("condition true")
else:
    print("condition false")